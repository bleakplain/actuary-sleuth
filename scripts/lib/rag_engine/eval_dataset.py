"""
RAG 评估数据集模块，覆盖事实题、多跳推理题、否定性查询、口语化查询、不可回答查询五种题型。
"""
import json
import logging
from dataclasses import dataclass, asdict, fields, field
from enum import Enum
from pathlib import Path
from typing import List, Optional

logger = logging.getLogger(__name__)

DEFAULT_DATASET_PATH = str(
    Path(__file__).parent / 'data' / 'eval_dataset.json'
)


class QuestionType(Enum):
    FACTUAL = "factual"
    MULTI_HOP = "multi_hop"
    NEGATIVE = "negative"
    COLLOQUIAL = "colloquial"
    UNANSWERABLE = "unanswerable"


class ReviewStatus(Enum):
    PENDING = "pending"
    APPROVED = "approved"


@dataclass(frozen=True)
class RegulationRef:
    doc_name: str
    article: str
    excerpt: str
    chunk_id: str = ""

    def to_dict(self) -> dict:
        return asdict(self)

    @classmethod
    def from_dict(cls, d: dict) -> 'RegulationRef':
        valid = {f.name for f in fields(cls)}
        return cls(**{k: v for k, v in d.items() if k in valid})


@dataclass(frozen=True)
class EvalSample:
    id: str
    question: str
    ground_truth: str
    evidence_docs: List[str]
    evidence_keywords: List[str]
    question_type: QuestionType
    difficulty: str
    topic: str
    regulation_refs: List[RegulationRef] = field(default_factory=list)
    review_status: ReviewStatus = ReviewStatus.PENDING
    reviewer: str = ""
    reviewed_at: str = ""
    review_comment: str = ""
    created_by: str = "human"
    kb_version: str = ""

    def to_dict(self) -> dict:
        d = asdict(self)
        d['question_type'] = self.question_type.value
        d['review_status'] = self.review_status.value
        d['regulation_refs'] = [r.to_dict() for r in self.regulation_refs]
        return d

    @classmethod
    def from_dict(cls, d: dict) -> 'EvalSample':
        valid = {f.name for f in fields(cls)}
        d = {k: v for k, v in d.items() if k in valid}
        d['question_type'] = QuestionType(d['question_type'])
        if 'review_status' in d and d['review_status']:
            d['review_status'] = ReviewStatus(d['review_status'])
        else:
            d['review_status'] = ReviewStatus.PENDING
        if 'regulation_refs' in d and d['regulation_refs']:
            d['regulation_refs'] = [RegulationRef.from_dict(r) for r in d['regulation_refs']]
        else:
            d['regulation_refs'] = []
        return cls(**d)


def load_eval_dataset(path: Optional[str] = None) -> List[EvalSample]:
    """从 JSON 文件加载评估数据集。默认路径不存在时回退到内置数据集并保存。"""
    if path is None:
        path = DEFAULT_DATASET_PATH

    try:
        with open(path, 'r', encoding='utf-8') as f:
            data = json.load(f)
    except FileNotFoundError:
        logger.info(f"评估数据集文件不存在: {path}，使用默认数据集")
        samples = create_default_eval_dataset()
        save_eval_dataset(samples, path)
        return samples

    if isinstance(data, list):
        items = data
    elif isinstance(data, dict) and 'samples' in data:
        items = data['samples']
    else:
        raise ValueError(f"不支持的评估数据集格式: {path}")

    return [EvalSample.from_dict(item) for item in items]


def save_eval_dataset(samples: List[EvalSample], path: Optional[str] = None) -> None:
    """将评估数据集保存为 JSON 文件。"""
    if path is None:
        path = DEFAULT_DATASET_PATH

    Path(path).parent.mkdir(parents=True, exist_ok=True)

    data = {
        'samples': [s.to_dict() for s in samples],
        'total': len(samples),
    }

    with open(path, 'w', encoding='utf-8') as f:
        json.dump(data, f, ensure_ascii=False, indent=2)

    logger.info(f"评估数据集已保存: {path} ({len(samples)} 条)")


def create_default_eval_dataset() -> List[EvalSample]:
    """创建默认评估数据集（覆盖五种题型，含 UNANSWERABLE）。"""
